package com.company.LivingRoom;

public class Rug {
    private String color;
    private int width;
    private int length;

    public Rug(String color, int width, int length) {
        this.color = color;
        this.width = width;
        this.length = length;
    }

    public String getColor() {
        return color;
    }

    public int getWidth() {
        return width;
    }

    public int getLength() {
        return length;
    }

}
